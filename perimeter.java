
public class perimeter {

	public static void main(String[] args) {
		int l=5;
		int b=7;
		int area=l*b;
		int per=(2*l)+(2*b);
		
		System.out.println("Area of rectangle=");
		System.out.println(area);
		System.out.println("Perimeter of rectangle=");
		System.out.println(per);
		

	}

}
